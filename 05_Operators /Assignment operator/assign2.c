#include<stdio.h>

int main(){
    int a = 25, b = 5; 
    printf("a = b : %d\n", a = b);
    printf("%d\n", a);
    printf("a += b : %d\n", a += b);    
    printf("a -= b : %d\n", a -= b);
    printf("a *= b : %d\n", a *= b);
    printf("a /= b : %d\n", a /= b);
    printf("a %%= b : %d\n", a %= b);
    printf("a &= b : %d\n", a &= b);
    printf("a |= b : %d\n", a |= b);
    printf("a ^= b : %d\n", a ^= b);
    printf("a <<= b : %d\n", a <<= b);
    printf("a >>= b : %d\n", a >>= b);
    

    return 0;
}
